void USART_TX_DMA_Send(UART_HandleTypeDef *huart,DMA_HandleTypeDef *hdma,uint8_t *buffer, uint16_t length)
{
    //等待上一次的数据发送完毕
	if(HAL_DMA_GetState(hdma) != HAL_DMA_STATE_READY)
	{
		return;
	}
    //关闭DMA
    __HAL_DMA_DISABLE(hdma);

    //开始发送数据
    HAL_UART_Transmit_DMA(huart, buffer, length);

}

//串口1的DMA发送printf
void usart1_printf(const char *format, ...)
{
	uint32_t length = 0;
	va_list args;
	
	__va_start(args, format);
	
	length = vsnprintf((char*)uart1_tx_buf, sizeof(uart1_tx_buf), (char*)format, args);
	
	USART_TX_DMA_Send(&huart1,&hdma_usart1_tx,uart1_tx_buf, length);
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	if (huart->Instance == USART1) //如果是串口1
	{
		// 在F7系列是可以不写的，F1必须写
		__HAL_DMA_CLEAR_FLAG(&hdma_usart1_tx, DMA_FLAG_TC4); //清除DMA2_Steam7传输完成标志
		HAL_UART_DMAStop(&huart1);		//传输完成以后关闭串口DMA,缺了这一句会死机
	}
	else if (huart->Instance == USART2) //如果是串口2
	{
		// 在F7系列是可以不写的，F1必须写
		__HAL_DMA_CLEAR_FLAG(&hdma_usart2_tx, DMA_FLAG_TC4); //清除DMA2_Steam7传输完成标志
		HAL_UART_DMAStop(&huart2);		//传输完成以后关闭串口DMA,缺了这一句会死机
	}
}

void HAL_UART_ReceiveIdle(UART_HandleTypeDef *huart)
{
	//当触发了串口空闲中断
    if((__HAL_UART_GET_FLAG(huart, UART_FLAG_IDLE) != RESET)) 
    {
			uint32_t tmp_flag = 0;
			uint32_t temp;
			if(huart->Instance == USART1)
			{
				/* 1.清除标志 */
				__HAL_UART_CLEAR_IDLEFLAG(huart); //清除空闲标志

				/* 2.读取DMA */
				HAL_UART_DMAStop(huart); //先停止DMA，暂停接收
				//这里应注意数据接收不要大于 USART_DMA_RX_BUFFER_MAXIMUM
				usart1_rx_len = UART1_Rx_Buffer_Max_Num - (__HAL_DMA_GET_COUNTER(&hdma_usart1_rx)); //接收个数等于接收缓冲区总大小减剩余计数
				
				/* 3.搬移数据进行其他处理 */
				memcpy(uart1_rec_data, uart1_rx_buf, usart1_rx_len); 
				
        if(uart1_rec_data[0] == 0xA1 && uart1_rec_data[usart1_rx_len-1] == 0xff)
        {
          usart1_rx_flag = 1; //标志已经成功接收到一包等待处理
        }
				
        
				memset(uart1_rx_buf,0,UART1_Rx_Buffer_Max_Num);//缓冲区清零
				
		
			}
    }
}

//串口1中断服务程序
void USART1_IRQHandler(void)
{
	HAL_UART_ReceiveIdle(&huart1);
	HAL_UART_IRQHandler(&huart1); //调用HAL库中断处理公用函数
	
	/* 4.重新打开串口DMA接收 */
	while (HAL_UART_Receive_DMA(&huart1,(uint8_t *)uart1_rx_buf, UART1_Rx_Buffer_Max_Num)!=HAL_OK); 
}

